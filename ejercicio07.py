
horas_de_trabajo = int(input("Cuantas horas trabaja al dia: "))
horas_a_la_semana = horas_de_trabajo * 7
pago_por_hora = int(input("Cuanto gana por hora: "))
salario_semanal = horas_a_la_semana * pago_por_hora

print("Horas de trabajo:", horas_de_trabajo)
print("Horas a la semana:", horas_a_la_semana)
print("Pago por hora:", pago_por_hora)
print("Salario semanal:", salario_semanal)